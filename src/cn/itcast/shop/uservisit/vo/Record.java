package cn.itcast.shop.uservisit.vo;

public class Record {
	private Integer recordid;
	private Integer uid;
	private Integer csid;
	private Integer csidvisit;
	
	
	
	public Integer getRecordid() {
		return recordid;
	}
	public void setRecordid(Integer recordid) {
		this.recordid = recordid;
	}
	public Integer getUid() {
		return uid;
	}
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	public Integer getCsid() {
		return csid;
	}
	public void setCsid(Integer csid) {
		this.csid = csid;
	}
	public Integer getCsidvisit() {
		return csidvisit;
	}
	public void setCsidvisit(Integer csidvisit) {
		this.csidvisit = csidvisit;
	}
	
}
