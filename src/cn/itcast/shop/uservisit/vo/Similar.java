package cn.itcast.shop.uservisit.vo;

public class Similar {
	private Integer similarid;
	private Integer uid;
	private Integer refuid;
	private Double similarvalue;
	
	public Integer getSimilarid() {
		return similarid;
	}
	public void setSimilarid(Integer similarid) {
		this.similarid = similarid;
	}
	public Integer getUid() {
		return uid;
	}
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	public Integer getRefuid() {
		return refuid;
	}
	public void setRefuid(Integer refuid) {
		this.refuid = refuid;
	}
	public Double getSimilarvalue() {
		return similarvalue;
	}
	public void setSimilarvalue(Double similarvalue) {
		this.similarvalue = similarvalue;
	}
	
}
